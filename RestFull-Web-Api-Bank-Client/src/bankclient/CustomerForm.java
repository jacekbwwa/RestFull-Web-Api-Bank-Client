    private Account currentAccount()
    {
        Account currentAccount = null;
        String item = (String)checkBoxAcc.getSelectedItem();
        for(int i = 0; i < acountArray.length; i++)
        {
            if(item.equals(acountArray[i].getAccountNumber()))
            {
                currentAccount= acountArray[i];
            }
        }
        return currentAccount;
    }

    private void btnBalanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBalanceActionPerformed
        int id = -1;
        // get id for account:
        String item = (String)checkBoxAcc.getSelectedItem();
        for(int i = 0; i < acountArray.length; i++)
        {
            if(item.equals(acountArray[i].getAccountNumber()))
            {
                id = acountArray[i].getId();
            }
        }
        
        String balance = BankClient.GetAccountBalance(id);
        
        if(balance == "null") balance = "0.00";
        JOptionPane.showMessageDialog(new JFrame(),
            "Balance is â‚¬" + balance);
    }//GEN-LAST:event_btnBalanceActionPerformed

